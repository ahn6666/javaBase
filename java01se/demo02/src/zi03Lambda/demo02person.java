package zi03Lambda;

public class demo02person {
    private String name;
    private Integer num;

    public demo02person() {

    }

    public demo02person(String name, Integer num) {
        this.name = name;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }
}
